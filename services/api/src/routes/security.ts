const formatModule = (
  module: SecurityModuleModel & { lessons: SecurityLesson[]; challenges: VibeChallenge[] },
  progress?: SecurityProgress | null,
) => {
  const topics = JSON.parse(module.topics || '[]') as string[];
  const lessons = module.lessons.sort((a, b) => a.sortOrder - b.sortOrder);
  const challenges = module.challenges.map((challenge) => ({
    ...challenge,
    hints: JSON.parse(challenge.hints || '[]'),
    heuristics: JSON.parse(challenge.heuristics || '[]'),
  }));
  const parsedProgress = progress
    ? {
        id: progress.id,
        moduleId: progress.moduleId,
        completedLessons: JSON.parse(progress.completedLessons || '[]') as string[],
        completedChallenges: JSON.parse(progress.completedChallenges || '[]') as string[],
        vibeConfidence: progress.vibeConfidence,
        notes: progress.notes,
        updatedAt: progress.updatedAt,
      }
    : null;

  return {
    ...module,
    topics,
    lessons,
    challenges,
    progress: parsedProgress,
  };
};

const analyzeSchema = z.object({
  code: z.string().min(20),
  url: z.string().url().optional(),
});

const analyzeSnippet = (code: string) => {
  const lines = code.split(/\r?\n/).filter((line) => line.trim().length > 0);
  const lineCount = lines.length;
  const avgLineLength = lines.reduce((sum, line) => sum + line.length, 0) / Math.max(lineCount, 1);
  const commentLines = lines.filter((line) => /\/\/|\/\*|<!--|-->/i.test(line)).length;
  const commentDensity = lineCount > 0 ? commentLines / lineCount : 0;
  const hasTodo = /TODO|FIXME|later lol/i.test(code);
  const hasExternalPaste = /(pastebin|gist\.githubusercontent|raw\.github)/i.test(code);
  const hasUniformClassPattern = /class(Name)?="[\w-]+(Container|Wrapper|Card|Title)"/g.test(code);
  const hasGenericCopy = /(start journey|best fits your success story|unlock potential)/i.test(code);
  const hasInlineStyle = /style\s*=\s*["']/.test(code);
  const minifiedLikely = /\)\}\);?$/.test(lines[lines.length - 1] ?? '') && lines.every((l) => !l.includes('  '));

  const signalScore =
    (hasExternalPaste ? 0.25 : 0) +
    (hasTodo ? 0.1 : 0) +
    (hasUniformClassPattern ? 0.15 : 0) +
    (hasGenericCopy ? 0.15 : 0) +
    (hasInlineStyle ? 0.1 : 0) +
    (minifiedLikely ? 0.15 : 0) +
    (commentDensity < 0.02 ? 0.1 : 0);

  const verdict =
    signalScore > 0.7
      ? 'Likely AI-generated or hastily stitched together'
      : signalScore > 0.4
        ? 'Mixed signals â€” review with a human teammate'
        : 'Feels handcrafted or at least reviewed by a human';

  const highlights = [
    hasExternalPaste && 'External script references (paste services) raise supply-chain questions.',
    hasUniformClassPattern && 'Uniform class naming pattern mirrors common boilerplate exports.',
    hasGenericCopy && 'Marketing copy reads like template filler.',
    hasInlineStyle && 'Inline styling on primary actions usually indicates rushed implementation.',
    minifiedLikely && 'Minified JSX suggests autogenerated output.',
    commentDensity < 0.02 && 'Scarce comments or rationale recorded in code.',
  ].filter(Boolean);

  return {
    score: Number(signalScore.toFixed(2)),
    verdict,
    metrics: {
      lineCount,
      avgLineLength: Number(avgLineLength.toFixed(1)),
      commentDensity: Number(commentDensity.toFixed(2)),
    },
    signals: highlights,
  };
};

router.get('/modules', async (_req, res, next) => {
  const req = _req as AuthedRequest;
  try {
    const modules = await prisma.securityModule.findMany({
      include: { lessons: true, challenges: true },
      orderBy: { createdAt: 'asc' },
    });
    let progressMap: Record<string, SecurityProgress> = {};
    if (req.user) {
      const progress = await prisma.securityProgress.findMany({
        where: { userId: req.user.id },
      });
      progressMap = progress.reduce<Record<string, SecurityProgress>>((acc, record) => {
        acc[record.moduleId] = record;
        return acc;
      }, {});
    }
    res.json({
      modules: modules.map((module) => formatModule(module, progressMap[module.id])),
    });
  } catch (error) {
    next(error);
  }
});

router.get('/modules/:id', async (req, res, next) => {
  try {
    const module = await prisma.securityModule.findUnique({
      where: { id: req.params.id },
      include: { lessons: true, challenges: true },
    });
    if (!module) {
      return res.status(404).json({ message: 'Module not found' });
    }
    let progress: SecurityProgress | null = null;
    if ((req as AuthedRequest).user) {
      progress = await prisma.securityProgress.findUnique({
        where: {
          moduleId_userId: {
            moduleId: module.id,
            userId: (req as AuthedRequest).user!.id,
          },
        },
      });
    }
    res.json({ module: formatModule(module, progress ?? undefined) });
  } catch (error) {
    next(error);
  }
});

router.get('/challenges', async (_req, res, next) => {
  try {
    const challenges = await prisma.vibeChallenge.findMany({
      include: {
        module: true,
      },
      orderBy: { createdAt: 'desc' },
    });
    res.json({
      challenges: challenges.map((challenge) => ({
        ...challenge,
        hints: JSON.parse(challenge.hints || '[]'),
        heuristics: JSON.parse(challenge.heuristics || '[]'),
        module: challenge.module ? formatModule({ ...challenge.module, lessons: [], challenges: [] }) : null,
      })),
    });
  } catch (error) {
    next(error);
  }
});

router.post('/analyze-code', async (req, res, next) => {
  try {
    const { code, url } = analyzeSchema.parse(req.body);
    const analysis = analyzeSnippet(code);
    res.json({
      analysis,
      metadata: {
        url,
        timestamp: new Date().toISOString(),
      },
    });
  } catch (error) {
    next(error);
  }
});

