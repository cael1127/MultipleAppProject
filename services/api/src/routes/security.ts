router.post('/progress', requireAuth, async (req: AuthedRequest, res, next) => {
  try {
    const schema = z.object({
      moduleId: z.string(),
      completedLessons: z.array(z.string()).optional(),
      completedChallenges: z.array(z.string()).optional(),
      vibeConfidence: z.number().min(0).max(1).optional(),
      notes: z.string().max(500).optional(),
    });
    const data = schema.parse(req.body);
    const progress = await prisma.securityProgress.upsert({
      where: {
        moduleId_userId: {
          moduleId: data.moduleId,
          userId: req.user!.id,
        },
      },
      update: {
        completedLessons: data.completedLessons ? JSON.stringify(data.completedLessons) : undefined,
        completedChallenges: data.completedChallenges ? JSON.stringify(data.completedChallenges) : undefined,
        vibeConfidence: data.vibeConfidence ?? undefined,
        notes: data.notes ?? undefined,
      },
      create: {
        moduleId: data.moduleId,
        userId: req.user!.id,
        completedLessons: JSON.stringify(data.completedLessons ?? []),
        completedChallenges: JSON.stringify(data.completedChallenges ?? []),
        vibeConfidence: data.vibeConfidence ?? 0,
        notes: data.notes ?? '',
      },
    });
    res.status(201).json({
      progress: {
        ...progress,
        completedLessons: JSON.parse(progress.completedLessons || '[]'),
        completedChallenges: JSON.parse(progress.completedChallenges || '[]'),
      },
    });
  } catch (error) {
    next(error);
  }
});

